CREATE TABLE TB_EXPECTED_EXPENSES (
	EXPECTED_EXPENSE_ID INT PRIMARY KEY AUTO_INCREMENT,
    EXPENSE_CATEGORY_ID INT,
    EXPECTED_EXPENSE_TYPE ENUM('VALUE', 'FORMULA'),
    TOTAL_VALUE DECIMAL(10,2)
);

CREATE TABLE TB_EXPECTED_EXPENSE_FORMULA (
	EXPECTED_EXPENSE_FORMULA_ID INT PRIMARY KEY AUTO_INCREMENT,
    EXPECTED_EXPENSE_ID INT,
    EXPECTED_EXPENSE_FORMULA_OPERATION ENUM('+', '-', '*', '/')
);

CREATE TABLE TB_EXPECTED_EXPENSE_FORMULA_ELEMENT (
	EXPECTED_EXPENSE_FORMULA_ELEMENT_ID INT PRIMARY KEY AUTO_INCREMENT,
    EXPECTED_EXPENSE_FORMULA_ID INT,
    EXPECTED_EXPENSE_FORMULA_ELEMENT_OPERATION ENUM('+', '-', '*', '/')	,
    EXPECTED_EXPENSE_FORMULA_ELEMENT_TYPE ENUM('VALUE', 'FORMULA'),
    TOTAL_VALUE DECIMAL(10,2),
    EXPENSE_CATEGORY_ID INT
);