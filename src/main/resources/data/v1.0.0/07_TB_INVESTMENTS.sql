CREATE TABLE TB_INVESTMENTS (
	INVESTMENT_ID INT PRIMARY KEY AUTO_INCREMENT,
    NAME VARCHAR(255),
    AMOUNT DECIMAL(10,2),
    EXPECTED_INCOME DECIMAL(10,2),
    MONTHLY_INVESTMENT DECIMAL(10,2)
);
/
CREATE TABLE TB_INVESTMENT_HISTORY (
	INVESTMENT_HISTORY_ID INT PRIMARY KEY AUTO_INCREMENT,
    INVESTMENT_ID INT,
    HISTORY_YEAR INT,
    HISTORY_MONTH INT,
    INITIAL_AMOUNT DECIMAL(10,2),
    FINAL_AMOUNT DECIMAL(10,2),
    MONTH_INCOME DECIMAL(10,2),
    AMOUNT_MOVED DECIMAL(10,2)
);